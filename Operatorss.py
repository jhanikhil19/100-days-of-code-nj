# Arithmetic Operators
print("5+6 is ",5+6)
print("5-6 is ",5-6)
print("5*6 is ",5*6)
print("5/6 is ",5/6) #actual divide
print("5%6 is ",5%6) #remainder
print("5**6 is ",5**6)
print("5//6 is ",5//6) #Quotient
# Assignment Operator
i=5
print(i)
i+=7 #there are many more such operators which can be searched over the internet comfortably.
print(i)

# Comparison Operator
j=19
print(j==7) # == != <=  >=
print(j==19)
# Logical Operator
# and or
a=True
b=False
print(a and b)
print(a or b)


# Identity Operator
a=True
b=False

print(a is b)
print(a is not b)

# Membership Operator
list1=[1,34,123,532,5432]
print(1 in list1)
print(1 not in list1)

# Bitwise Operator

print (0 & 1)
print(0 | 1)
print (0 & 3)
print (3 & 4)
print (3 | 4)

# Operator	Example	    Meaning
# &	        a & b	    Bitwise AND
# | 	    a | b	    Bitwise OR
# ^	        a ^ b	    Bitwise XOR (exclusive OR)
# ~	        ~a	        Bitwise NOT
# <<	    a << n	    Bitwise left shift
# >>	    a >> n	    Bitwise right shift

